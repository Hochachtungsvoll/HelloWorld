public class Switch {

    public static void main (String [] args) {

        int temperatur = 7;

        switch(temperatur){

            case 0: System.out.println("Es sind null Grad!"); break;
            case 1: System.out.println("Es ist 1 Grad!"); break;
            case 2: System.out.println("Es sind 2 Grad!"); break;
            case 3: System.out.println("Es sind 3 Grad!"); break;
            case 4: System.out.println("Es sind 4 Grad!"); break;
            case 5: System.out.println("Es sind 5 Grad!"); break;
            case 6: System.out.println("Es sind 6 Grad!"); break;
            case 7: System.out.println("Es sind 7 Grad! Zeit zum chillen!"); break;
            default: System.out.println("Ne, ich gehe jetzt nicht raus!"); break;
        }


    }


}
