public class IfElse {

    public static void main (String [] args){

        int temperatur;
        temperatur = 7;

        if (temperatur < 7){
            System.out.println("Ich gehe nicht raus!");
        } else if (temperatur == 7){
            System.out.println("Optimal!");
        } else {
            System.out.println("Ich gehe raus!");
        }


    }

}
